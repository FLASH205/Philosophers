#include "philo.h"

int	ft_atoi(char *str)
{
	int		i;
	long	r;

	i = 0;
	r = 0;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		r = r * 10 + str[i] - '0';
		if (r > 2147483647)
			return (0);
		i++;
	}
	return (r);
}
